// Test the math abilities of this program
const a = 30;
const b = 50;
const c = a + b;
console.log(c);
console.assert(c == 80);
console.log("First assertion passed!");

// Do it again
const a1 = 4;
const b1 = 8;
const c1 = a1 + b1;
console.assert(c1 == 12);
console.log("Second assertion passed!");

// Do it again, but with substraction
const a2 = 8;
const b2 = 4;
const c2 = a2 - b2;
console.assert(c2 == 4);
console.log("Third assertion passed!");

